/// Defines Spacing in app UI.
abstract class AppSpacing {
  /// default unit of spacing.
  static const spaceUnit = 16.0;

  /// xs spacing value (4pt).
  static const xs = 0.25 * spaceUnit;

  /// sm spacing value (8pt).
  static const sm = 0.5 * spaceUnit;

  /// md spacing value (12pt).
  static const md = 0.75 * spaceUnit;

  /// lg spacing value (16pt)
  static const lg = spaceUnit;

  /// xlg spacing value (24pt).
  static const xlg = 1.5 * spaceUnit;

  /// xxlg spacing value (32pt)
  static const xxlg = 2 * spaceUnit;

  /// xxxlg spacing value (64pt).
  static const xxxlg = 4 * spaceUnit;
}
