import 'package:flutter/material.dart';

/// Namespace for default App Font Weight.
abstract class AppFontWeight {
  /// Font Weight value of `w900`.
  static const black = FontWeight.w900;

  /// Font Weight value of `w800`.
  static const extraBold = FontWeight.w800;

  /// Font Weight value of `w700`.
  static const bold = FontWeight.w700;

  /// Font Weight value of `w600`.
  static const semiBold = FontWeight.w600;

  /// Font Weight value of `w500`.
  static const medium = FontWeight.w500;

  /// Font Weight value of `w400`.
  static const regular = FontWeight.w400;

  /// Font Weight value of `w300`.
  static const light = FontWeight.w300;

  /// Font Weight value of `w200`.
  static const extraLight = FontWeight.w200;

  /// Font Weight value of `w100`.
  static const thin = FontWeight.w100;
}
